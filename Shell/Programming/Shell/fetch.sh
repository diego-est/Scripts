PURPLE=$(   tput bold setaf 5)
NORMAL=$(   tput sgr0)
echo ""
echo "  ${YELLOW}void${NORMAL}@${YELLOW}The Void${NORMAL}"
echo "  ${GREEN}Distro  ${NORMAL}~ ${GREEN}Void Linux${NORMAL}"
echo "  ${AQUA}Shell   ${NORMAL}~ ${AQUA}zsh${NORMAL}"
echo "  ${BLUE}Term    ${NORMAL}~ ${BLUE}kitty${NORMAL}"
echo "  ${PURPLE}WM      ${NORMAL}~ ${PURPLE}i3${NORMAL}"
echo "  ${RED}▅▅▅${YELLOW}▅▅▅${GREEN}▅▅▅${AQUA}▅▅▅${BLUE}▅▅▅${PURPLE}▅▅▅${NORMAL}"
